for (var i = 1; i <= 100; i++) {
    if (i % 3 === 0 && i % 5 === 0) {      //both of them true to read the next output code 
        console.log("FizzBuzz");
    } else if (i % 3 === 0) {
        console.log("Fizz");
    } else if (i % 5 === 0) {
        console.log("Buzz");
    } else {
        console.log(i);
    }
}


